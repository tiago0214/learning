# função print. Recebe Argumentos.
# passei , argumentos não nomeados.
# o separador(sep=), vem automaticamente , por espaço. (isso é um argumento nomeado).
# esta com quebra de linha automática sendo executada, posso alterar
# (end) significa o final da linha do print.(isso é um argumento nomeado.)
# para fazer isso no windows \r\n
print(1, 2, 3, sep="-", end="\n#")  # padrão
print(4, 5, 6, sep="-", end="//" )  # retirei a quebra
print(8, 9, 10, sep="-")
