# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado
# positivo.
# print(11) # int
# print(-11) # int
print(type(0))

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
# print(1.1, 10.11)
# print(0.0, -1.5)

# A função type mostra o tipo que o Python
# inferiu ao valor.

# inteiros, float, string: são tipos primivitivos no python.

# nesse ex: eu vou executar a funcão print e vou executar dentro dela,outro valor. (função print vai me retornar o valor da outra função dentro dela.)
#  função no python: definida com letra minuscula e com () e é executavel.
# tudo em python é um objeto.
print(type("qualquer coisa"))
print(type(1))
print(type(1.1), type(-1.3), type(0.0))
